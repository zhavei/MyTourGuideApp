package com.example.mytourguideapp;

public class DataModelGunung {

    private String namaGunung;
    private int imageUrl;
    private String tinggiGungung;
    private String deskripsiGunung;

    public String getNamaGunung() {
        return namaGunung;
    }

    public void setNamaGunung(String namaGunung) {
        this.namaGunung = namaGunung;
    }

    public int getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(int imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getTinggiGungung() {
        return tinggiGungung;
    }

    public void setTinggiGungung(String tinggiGungung) {
        this.tinggiGungung = tinggiGungung;
    }

    public String getDeskripsiGunung() {
        return deskripsiGunung;
    }

    public void setDeskripsiGunung(String deskripsiGunung) {
        this.deskripsiGunung = deskripsiGunung;
    }
}